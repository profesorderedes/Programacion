
public class Ejemplo15 {
	
	public static void main(String[] args) {
		
		int contador = 1;
		
		while(contador <= 5) {
			
			System.out.println(contador + ") No por mucho madrugar amanece "
					+ "más temprano.");
			
			contador = contador + 1;
		}
		
		System.out.println("\nDe nada.");
		
	}

}
