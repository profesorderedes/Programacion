
public class Ejemplo11 {

	public static void main(String[] args) {

		// Vamos a mostrar los números del 1 al 10.

		// Para hacer de contador, solemos usar una variable con
		// nombre i, j o k (un convenio).
		int i = 1;

		// Mientras la instrucción entre paréntesis sea cierta,
		// se ejecutará el bloque interior al while.
		while (i <= 10) {

			System.out.println(i);
			i++; // i = i + 1

		}

		System.out.println("De nada.");

	}

}
