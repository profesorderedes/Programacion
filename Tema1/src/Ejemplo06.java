
public class Ejemplo06 {

	public static void main(String[] args) {

		// Los Strings o cadenas de caracteres no son tipos
		// primitivos, sino objetos.

		String mensaje = "Hola a todos.";

		// Como objeto, mensaje tiene métodos a los que podemos
		// llamar, como toUpperCase(), toLowercase() o charAt().
		System.out.println(mensaje.charAt(0));

	}

}
