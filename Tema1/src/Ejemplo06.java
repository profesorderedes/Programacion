
public class Ejemplo06 {
	
	public static void main(String[] args) {
		
		// Los char se guardan como números enteros,
		// y podemos hacer algunas operaciones con ellos.
		
		char letra = 'M'; // Su código ASCII es 77
		
		System.out.println(letra + 1);
		
		System.out.println((char)(letra + 1));
		
	}

}
