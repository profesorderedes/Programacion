
public class Ejemplo11 {

	public static void main(String[] args) {

		int a = 5, b = 10, c = 5;

		// Operadores relacionales.
		
		System.out.println("¿a == b? " + (a == b));
		System.out.println("¿a == c? " + (a == c));

		System.out.println("¿a != b? " + (a != b));

		System.out.println("¿a < b? " + (a < b));
		System.out.println("¿a < c? " + (a < c));
		System.out.println("¿a <= c? " + (a <= c));
	}

}
