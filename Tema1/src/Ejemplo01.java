   
public class Ejemplo01 {
	
	public static void main(String[] args) {
		
		// Tipo booleano.
		boolean comprar = false;
		boolean mayorDeEdad = true;
		
		// Tipos enteros.
		byte alumnos = 26;
		short numPeliculas = 1000;
		int habitantes = 700000;
		long numCuenta = 27991028086780L;
		
		System.out.println("Número de cuenta: " + habitantes);
		
		// Tipos decimales.
		float temperatura = 20.5F;
		double precio = 1200.5;
		
		System.out.println("Precio: " + precio + " €.");
		
		// Tipo char.
		char defcon = '2';
		char letraDni = 'E';
		
		// Tipo cadena de caracteres (no es un tipo primitivo, es
		// un objeto).
		String nombre = "Francisco Pérez";
		
	}

}
