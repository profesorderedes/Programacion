package hoja1;

public class Ejer05 {

	public static void main(String[] args) {

		double[] notas = new double[5];

		notas[0] = 8.2;
		notas[1] = 8.6;
		notas[2] = 8.4;
		notas[3] = 10;
		notas[4] = 8;

		int i = 0;

		while (i < notas.length) {

			System.out.println(notas[i] + " ");
			i++;

		}

	}

}
