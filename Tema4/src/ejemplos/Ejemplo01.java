package ejemplos;

public class Ejemplo01 {

	public static void main(String[] args) {

		System.out.println("Vamos a llamar al método saludar:");
		saludar();

		System.out.println("¿Qué te parece?");

		despedir();

	}

	static void saludar() {

		System.out.println("Buenas tardes, ¿cómo estás?");

	}

	static void despedir() {

		System.out.println("Hasta mañana.");

	}

}
